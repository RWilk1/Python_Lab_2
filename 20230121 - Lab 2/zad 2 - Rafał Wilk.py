

# 2. Pobierz 2 lizby i wykonaj na nich obliczenia

Pierwsza_Liczba = int(input("Podaj pierwszą liczbę: "))
Druga_Liczba = int(input("Podaj drugą liczbę: "))

print("Dodawanie: ", Pierwsza_Liczba + Druga_Liczba)
print("Odejmowanie: ", Pierwsza_Liczba - Druga_Liczba)
print("Mnożenie: ", Pierwsza_Liczba * Druga_Liczba)
print("Dzielenie: ", Pierwsza_Liczba / Druga_Liczba)
print("Moduło: ", Pierwsza_Liczba % Druga_Liczba)